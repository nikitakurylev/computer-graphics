#pragma once

// ============================================================================
// BTNodeState.h
// Базовые перечисления и типы для AI системы
// ============================================================================

namespace AI {

    // ------------------------------------------------------------------------
    // Состояния выполнения узлов Behaviour Tree
    // ------------------------------------------------------------------------
    enum class BTNodeState {
        // Узел успешно выполнен
        Success,

        // Узел завершился с ошибкой/неудачей
        Failure,

        // Узел все еще выполняется (асинхронная операция)
        // Будет продолжен на следующем кадре
        Running,

        // Узел был прерван принудительно
        // Используется для остановки дерева или ветви
        Aborted
    };

    // ------------------------------------------------------------------------
    // Приоритет действий для Utility AI
    // Используется для сортировки действий по важности
    // ------------------------------------------------------------------------
    enum class ActionPriority {
        VeryLow = 0,
        Low = 1,
        Normal = 2,
        High = 3,
        VeryHigh = 4,
        Critical = 5
    };

    // ------------------------------------------------------------------------
    // Вспомогательная функция: конвертация состояния в строку для отладки
    // ------------------------------------------------------------------------
    inline const char* BTNodeStateToString(BTNodeState state) {
        switch (state) {
        case BTNodeState::Success: return "Success";
        case BTNodeState::Failure: return "Failure";
        case BTNodeState::Running: return "Running";
        case BTNodeState::Aborted: return "Aborted";
        default: return "Unknown";
        }
    }

} // namespace AI